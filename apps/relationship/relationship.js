    function isRelation(ch) {
        return relationChars.indexOf(ch) !== -1;
    }

    function getRelation(current, what) {
        var map = dataMap[current];
        if (!map) return null;
        return map[what] || null;
    }

    function isPossible(result) {
        return result && result.charAt(0) === "<";
    }

    function parsePossible(str) {
        var matches = [];
        var re = /<([^>]+)>/g;
        var m;
        while ((m = re.exec(str)) !== null) {
            matches.push(m[1]);
        }
        return matches;
    }

    function showModal(message) {
        modalText.textContent = message;
        modalOverlay.classList.add("active");
        return new Promise(function (resolve) {
            pendingResolve = resolve;
        });
    }

    function getCompareTarget(older) {
        if (older === "伯伯" || older === "伯母") return "爸爸";
        if (older === "大伯") return "老公";
        if (older === "大姑子") return "老公";
        if (older === "大舅") return "老婆";
        if (older === "大姨") return "妈妈";
        return "你";
    }

    async function handleButton(action) {
        switch (action) {
            case "AC":
                now = "你";
                stack = [];
                inputEl.value = "";
                outputEl.value = "";
                break;
            case "=":
                outputEl.value = now;
                break;
            case "←":
                var text = inputEl.value;
                if (text.length >= 1) {
                    var removed = text.charAt(text.length - 1);
                    inputEl.value = text.substring(0, text.length - 1);
                    if (stack.length > 0 && isRelation(removed)) {
                        now = stack.pop();
                        outputEl.value = now;
                    }
                }
                break;
            case "的":
                inputEl.value += "的";
                break;
            default:
                if (isRelation(action)) {
                    inputEl.value += action;
                    stack.push(now);
                    var result = getRelation(now, action);
                    if (isPossible(result)) {
                        var options = parsePossible(result);
                        if (options.length === 2) {
                            var target = getCompareTarget(options[0]);
                            var isOlder = await showModal("是否比" + target + "大？");
                            result = isOlder ? options[0] : options[1];
                        }
                    }
                    if (result && result !== "不存在") {
                        now = result;
                        outputEl.value = now;
                    } else if (result === "不存在") {
                        now = "不存在";
                        outputEl.value = "该关系不存在";
                    } else {
                        outputEl.value = "超出计算范围";
                    }
                }
                break;
        }
    }

